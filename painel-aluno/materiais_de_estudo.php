<h3>O que é</h3>
<br><p>O <span style="font-weight: bold;">Ângulo</span> é a medida da abertura entre dois segmentos de reta. Desse modo, existe um número que está relacionado com cada abertura entre duas semirretas e, quanto maior a abertura, maior esse número. Ele é uma medida expressa em graus que é atribuível à região ou conjunto de pontos situados entre duas semirretas de mesma origem. Geralmente os ângulos são representados por letras maiúsculas com acento circunflexo, por letras minúsculas ou, no caso da figura abaixo, da seguinte maneira: BÂC.

