<h2>Medindo ângulos</h2>
<p>As medidas atribuídas aos ângulos funcionam de forma diferente daquelas utilizadas para medir distâncias. Os ângulos têm o círculo como base. Ao aumentar um ângulo, uma das semirretas se deslocará, como se estivesse sobre um círculo em que o ponto de encontro delas é o centro. Por isso, não é possível utilizar uma régua para obter medidas de ângulos.O equipamento utilizado para tomar medidas de ângulos é conhecido como transferidor.</p>

<center>
<figure class="foto-legenda">
	<img src = "../img/transferidor.webp"/>
	<br>Transferidor</br>
</figure></center>

<p>Para utilizá-lo, coloque uma das semirretas sobre a primeira linha do transferidor, aquela que aponta para o zero. Depois, coloque o ponto de encontro das semirretas no centro do equipamento, que geralmente vem marcado nele. Feito isso, o ângulo a ser medido será o número para onde a segunda semirreta aponta..</p>

