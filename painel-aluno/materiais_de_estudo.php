<h2>Os ângulos notáveis</h2>
<br><p>Alguns ângulos são mais observados pelo homem na natureza. Foram eles que deram origem à escolha específica dos números utilizados para medir os ângulos. Ao ângulo conhecido como raso, por exemplo, que é definido quando uma semirreta é mantida fixa e a outra descreve um movimento de meia volta, foi atribuído o valor 180°.</p></br>

<center>
<figure class="foto-legenda">
	<img src = "../img/exemplo-de-angulo-raso.webp"/>
	<br>Ângulo formado por meia-volta: ângulo de 180°</br>
</figure></center>

<br><p>Uma propriedade interessante do ângulo raso é que as semirretas que o formam, ao serem ligadas, podem ser vistas como uma única reta, ou seja, dado um ponto em uma reta, o ângulo formado nesse ponto é 180°.

Acredita-se que esses valores foram escolhidos em uma época onde os homens acreditavam que o ano possuía 360 dias. Cada dia foi considerado como uma unidade de medida do ângulo descrito pela Terra ao redor do sol e, por isso, uma volta inteira seria 360°.

Outro ângulo importante é conhecido como ângulo reto e sua medida é igual a 90°. Esse ângulo é muito encontrado na construção civil, nas “quinas” formadas por duas paredes. Sua importância é tão grande que existe uma ferramenta criada exclusivamente para ajudar a construir esse tipo de “quina” e para medir esse ângulo: o Esquadro.</p></br>

